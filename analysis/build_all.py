def _read(family):
    p = RES / family / "results.csv"
    if not p.exists():
        return None
    df = pd.read_csv(p)
    return df


def _fmt(x, nd=2):
    if x is None or (isinstance(x, float) and not np.isfinite(x)):
        return None
    return round(float(x), nd)


def _ci(point, lo, hi, nd=2):
    """LaTeX-ready point estimate with a 95% CI, en-dash range."""
    return "%.*f (95\\%% CI %.*f--%.*f)" % (nd, point, nd, lo, nd, hi)


def _cipct(point, lo, hi, nd=0):
    """Percentage point estimate with a 95% CI (CI bounds unsuffixed)."""
    return "%.*f\\%% (95\\%% CI %.*f--%.*f)" % (nd, point, nd, lo, nd, hi)


def _read_json(path):
    return json.load(open(path)) if path.exists() else None


def policy_methods(df, arch):
    pref = "v2%s" % arch
    return sorted({m for m in df.method.unique()
                   if isinstance(m, str) and m.startswith(pref)})



def write_pvalue_table(gates_out):
    """Generated Gate P p-value appendix table (protocol log 5 promise)."""
    gp = gates_out.get("gate_p")
    if not gp:
        return
    rows = []
    pretty = {"edd": "EDD", "wspt": "WSPT", "atc": "ATC", "pfifo": "pFIFO",
              "mor": "LPT", "lfj_atc": "LFJ-ATC", "atc_eta": "ATC-$\\eta$"}
    lines = ["%% GENERATED by analysis/build_all.py -- do not edit.",
             "\\begin{table}[htbp]",
             "\\caption{Gate P paired Wilcoxon tests: policy seed-mean vs "
             "each ranked rule, raw and Holm-adjusted p-values per scope, "
             "and the winner of each comparison on the pooled means (R = "
             "rule lower, P = policy lower). Paired effect sizes "
             "(rank-biserial, with CIs) for the pooled scope are in "
             "Section~\\ref{sec:results-gatep} and the released tables. "
             "$n$ = common instance-configurations "
             "per scope.}",
             "\\label{tab:pvalues}", "\\centering", "\\footnotesize",
             "\\begin{tabular*}{\\tblwidth}{@{}l@{\\extracolsep{\\fill}}"
             "rrcrrcrrc@{}}",
             "\\toprule",
             "& \\multicolumn{3}{c}{pooled} & \\multicolumn{3}{c}{$m=0.8$} "
             "& \\multicolumn{3}{c}{$m=0.6$} \\\\",
             "Rule & raw & Holm & win & raw & Holm & win & raw & Holm & "
             "win \\\\",
             "\\midrule"]
    def fp(x):
        return ("$<$0.001" if x < 0.001 else "%.3f" % x)
    for r in ["edd", "wspt", "atc", "pfifo", "mor", "lfj_atc", "atc_eta"]:
        cells = [pretty[r]]
        for scope in ("pooled", "m08", "m06"):
            rr = gp[scope]["rules"].get(r)
            if rr:
                cells += [fp(rr["wilcoxon_p_raw"]),
                          fp(rr["wilcoxon_p_holm"]),
                          ("P" if rr.get("policy_lower") else "R")]
            else:
                cells += ["--", "--", "--"]
        lines.append(" & ".join(cells) + " \\\\")
    n_note = " / ".join("%s: %d" % (sc, gp[sc]["n_configs"])
                        for sc in ("pooled", "m08", "m06"))
    lines += ["\\bottomrule", "\\end{tabular*}",
              "\\par\\smallskip {\\footnotesize $n$ = %s.}" % n_note,
              "\\end{table}"]
    with open(PAPER / "sections" / "gen_pvalues.tex", "w") as f:
        f.write("\n".join(lines) + "\n")


def write_feasibility_table(r3):
    """Feasibility-scenario table: loose / normal-qualification / strict-
    licensing skill-adjacency, with workload coverage, capture, and the
    Gate C verdict per scenario."""
    if r3 is None or "feasibility" not in r3:
        return
    fz = r3["feasibility"]
    rows = [
        ("Illustrative unrestricted", "loose",
         "every trade cross-trainable"),
        ("Illustrative moderately constrained", "normal",
         "e.g.\\ electrical, elevators"),
        ("Illustrative restrictive", "strict",
         "e.g.\\ + plumbing, fire"),
    ]
    lines = [
        "%% GENERATED by analysis/build_all.py -- do not edit.",
        "\\begin{table}[htbp]",
        "\\caption{Illustrative feasibility scenarios for the "
        "one-secondary-skill chain at $m = 0.6$: how the capture ratio moves "
        "as an eligibility constraint tightens. These scenarios are designed "
        "inputs, not measured regulation; a facility would substitute its own "
        "eligibility matrix. Coverage = share of p95 "
        "workload in trades that may receive a helper; capture is on the "
        "rules envelope at $\\eta = 1.0$; Gate C requires "
        "$\\rho \\geq 0.70$. The chain suffices under the unrestricted and "
        "moderately constrained scenarios and tracks coverage down as the "
        "restrictive scenario removes it. $n$ = 763 instances per cell; "
        "verdict campuses.}",
        "\\label{tab:feasibility}", "\\centering", "\\small",
        "\\begin{tabular*}{\\tblwidth}{@{}l@{\\extracolsep{\\fill}}"
        "llll@{}}",
        "\\toprule",
        "Illustrative scenario & Example ineligible trades & Coverage "
        "& $\\rho$ (env.) & Gate C \\\\",
        "\\midrule"]
    for name, key, gated in rows:
        b = fz.get(key)
        if not b:
            continue
        lines.append("%s & %s & %.0f\\%% & %.2f & %s \\\\" % (
            name, gated, 100 * b["coverage"], b["capture_env_eta10"],
            "passes" if b["passes_gatec"] else "fails"))
    lines += ["\\bottomrule", "\\end{tabular*}", "\\end{table}"]
    with open(PAPER / "sections" / "gen_feasibility.tex", "w") as f:
        f.write("\n".join(lines) + "\n")


