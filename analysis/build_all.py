    if branch_path.exists():
        bn = json.load(open(branch_path))
        for k, v in bn.items():
            if v:
                numbers[k] = v
    with open(PAPER / "numbers.json", "w") as f:
        json.dump(numbers, f, indent=1, sort_keys=True)

    with open(PAPER / "numbers.tex", "w") as f:
        f.write("%% GENERATED by analysis/build_all.py -- do not edit.\n")
        for k, v in sorted(numbers.items()):
            f.write("\\expandafter\\def\\csname dat@%s\\endcsname{%s}\n"
                    % (k, v))
    print("numbers.json: %d keys resolved" % len(numbers))

    if args.figures:
        from analysis import figures
        figures.build_all(numbers)
        print("figures built")


